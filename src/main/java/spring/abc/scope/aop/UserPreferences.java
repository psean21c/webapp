package spring.abc.scope.aop;

public class UserPreferences {

	String preferName;

	public UserPreferences(String preferName) {
		this.preferName = preferName;
	}

	public String getPreferName() {
		return preferName;
	}

	public void setPreferName(String preferName) {
		this.preferName = preferName;
	}
	
}
